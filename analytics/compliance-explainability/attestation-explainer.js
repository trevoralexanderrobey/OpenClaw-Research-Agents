"use strict";

const crypto = require("node:crypto");
const fs = require("node:fs/promises");
const path = require("node:path");
const { nowIso } = require("../../openclaw-bridge/core/time-provider.js");
const { buildGateRationale, canonicalize } = require("./gate-rationale.js");

function safeString(value) {
  return typeof value === "string" ? value.trim() : "";
}

function canonicalJson(value) {
  return `${JSON.stringify(canonicalize(value), null, 2)}\n`;
}

function sha256(value) {
  return crypto.createHash("sha256").update(String(value || "")).digest("hex");
}

function asArray(value) {
  return Array.isArray(value) ? value : [];
}

function getLatestBySequence(value) {
  return asArray(value)
    .slice()
    .sort((left, right) => Number(right.sequence || 0) - Number(left.sequence || 0))[0] || null;
}

function buildComplianceExplainabilityReport(input = {}) {
  const asOfIso = safeString(input.asOfIso);
  const evaluation = input.evaluation && typeof input.evaluation === "object" ? input.evaluation : {};
  const rationale = buildGateRationale({ evaluation });

  const markdownLines = [
    "# Phase 8 Compliance Explainability Report",
    "",
    "AI-assisted analysis disclosure: This report is generated by deterministic internal compliance analytics. External submission remains human-only.",
    "",
    `Generated At: ${asOfIso}`,
    `Target Ref: ${safeString(evaluation.targetRef)}`,
    `Target SHA: ${safeString(evaluation.targetSha)}`,
    `Recommended Decision: ${safeString(evaluation.decision)}`,
    `Reason Code: ${safeString(evaluation.reasonCode)}`,
    "",
    "## Ranked Factors",
    "| Rank | Factor | Value | Impact |",
    "| ---: | --- | ---: | ---: |",
    ...rationale.factors.map((factor) => `| ${Number(factor.rank)} | ${safeString(factor.label)} | ${String(factor.value)} | ${Number(factor.impact)} |`),
    "",
    "## Missing Required Checks",
    ...(rationale.missingChecks.length > 0
      ? rationale.missingChecks.map((check) => `- ${check}`)
      : ["- none"]),
    ""
  ];

  return {
    ok: true,
    asOfIso,
    rationale,
    markdown: `${markdownLines.join("\n")}\n`
  };
}

async function writePhase8Artifacts(input = {}) {
  const apiGovernance = input.apiGovernance;
  if (!apiGovernance || typeof apiGovernance.readState !== "function") {
    throw new Error("apiGovernance.readState is required");
  }

  const asOf = safeString(input.asOfIso) || String(nowIso());
  const outDir = path.resolve(input.outDir || path.join(process.cwd(), "audit", "evidence", "phase8"));
  await fs.mkdir(outDir, { recursive: true });

  const state = await apiGovernance.readState();
  const governance = state && state.complianceGovernance && typeof state.complianceGovernance === "object"
    ? state.complianceGovernance
    : {};

  const latestAttestation = getLatestBySequence(governance.attestationSnapshots) || {};
  const latestBundle = getLatestBySequence(governance.evidenceBundles) || {};
  const latestDecision = getLatestBySequence(governance.releaseGates) || {};

  let evaluation = {
    decision: "hold",
    reasonCode: "policy_violation",
    targetRef: safeString(input.targetRef),
    targetSha: safeString(input.targetSha),
    asOfIso: asOf,
    policySnapshotHash: safeString(latestBundle.policySnapshotHash),
    missingChecks: []
  };

  if (input.releaseGateGovernor && typeof input.releaseGateGovernor.evaluateReleaseGate === "function") {
    evaluation = await input.releaseGateGovernor.evaluateReleaseGate({
      targetRef: safeString(input.targetRef) || safeString(latestDecision.targetRef),
      targetSha: safeString(input.targetSha) || safeString(latestDecision.targetSha),
      asOfIso: asOf
    });
  }

  const explanation = buildComplianceExplainabilityReport({
    asOfIso: asOf,
    evaluation
  });

  const files = {
    "runtime-attestation.json": canonicalJson({
      policyVersion: safeString(governance.policyVersion) || "v1",
      latest: latestAttestation,
      records: asArray(governance.attestationSnapshots)
    }),
    "compliance-bundle.json": canonicalJson({
      latest: latestBundle,
      records: asArray(governance.evidenceBundles)
    }),
    "release-gate-evaluation.json": canonicalJson({
      evaluation
    }),
    "release-gate-decisions.json": canonicalJson({
      decisions: asArray(governance.releaseGates)
    }),
    "compliance-ledger-chain.json": canonicalJson({
      decisionLedger: governance.decisionLedger || { records: [], nextSequence: 0, chainHead: "" }
    }),
    "compliance-explainability-report.md": explanation.markdown
  };

  const manifestEntries = [];
  for (const [name, body] of Object.entries(files)) {
    await fs.writeFile(path.join(outDir, name), body, "utf8");
    manifestEntries.push({
      file: name,
      sha256: sha256(body)
    });
  }

  const manifestBody = canonicalJson({
    generatedAt: asOf,
    files: manifestEntries.sort((left, right) => left.file.localeCompare(right.file))
  });
  await fs.writeFile(path.join(outDir, "phase8-hash-manifest.json"), manifestBody, "utf8");

  return {
    ok: true,
    outDir,
    files: [...Object.keys(files), "phase8-hash-manifest.json"].sort()
  };
}

module.exports = {
  buildComplianceExplainabilityReport,
  writePhase8Artifacts,
  canonicalJson,
  sha256
};
